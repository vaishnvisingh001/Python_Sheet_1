Day = int(input("Enter a no. to know the day of the Week(1-7) : "))

if(Day == 1):
    print("Today is Sunday")
elif(Day == 2):
    print("Today is Monday")
elif(Day == 3):
    print("Today is Tuesday")
elif(Day == 4):
    print("Today is Wednesday")
elif(Day == 5):
    print("Today is Thursday")
elif(Day == 6):
    print("Today is Friday")
elif(Day == 7):
    print("Today is Saturday")
else:
    print("Invalid day plss enter no. from(1-7)")